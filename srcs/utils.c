#include "../includes/philo.h"

int	ft_atoi(char *str)
{
	int		i;
	int		sig;
	long	n;

	i = 0;
	n = 0;
	sig = 0;
	while (str[i] == ' ' || str == '\t')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sig = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return ((int)n * sign);
}
